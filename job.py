
class Job:
    def __init__(self, job_id, arrival_time, operations):
        self.job_id = job_id  # Unique identifier for the job
        self.arrival_time = arrival_time  # Time at which the job arrives in the system
        self.operations = operations  # List of operations that the job will perform (CPU, I/O)
        self.remaining_operations = operations[:]  # Copy of operations to track remaining tasks
        self.completed_operations = 0  # Counter for completed operations
        self.total_interrupts = 0  # Track total interrupts
        self.completed = False  # Flag to mark if the job is completed
        self.completion_time = 0  # Time when the job is finished
        self.turnaround_time = 0  # Turnaround time (completion time - arrival time)
        self.waiting_time = 0  # Waiting time (time spent waiting in the ready queue)
        self.io_time_remaining = 0  # Time left for ongoing I/O
        self.first_op_type_io = False  # Flag to track if the first operation is I/O
        self.remaining_context_switch = 0  # Track context switches remaining for the job

    # Will return the first operation of the Job
    # if no operation left, retun None
    def get_current_operation(self):
        # Get the first operation (CPU or I/O) and its duration
        return self.remaining_operations[0] if self.remaining_operations else None

    # Perform the current CPU operation up to the time quantum
    def perform_cpu_operation(self, time_quantum):
        # No remaining operations to perform
        if not self.remaining_operations:
            return 0  

        op_type, cycles = self.remaining_operations[0]

        # Check again if the operation is CPU
        if op_type == 'CPU':
            # Choose how many CPU cycle need to processed
            processed_cycles = min(cycles, time_quantum)

            # If the the CPU cycle needed is less than or equal to time quantum
            if processed_cycles == cycles:
                self.remaining_operations.pop(0)  # Remove the completed operation from the list
                self.completed_operations += 1  # Increment completed operations counter
            # If the CPU cycle needed is more than time quantum
            else:
                # Update remaining cycles
                self.remaining_operations[0] = (op_type, cycles - processed_cycles)
            return processed_cycles  # Return the number of cycles processed during this time quantum
        return 0  # If it's not a CPU operation, return 0

    # This function moves the job's I/O operation from the list of remaining operations
    # and added the I/O cycle to job's io_time_remaining.
    def start_io_operation(self):
        # No remaining operations to perform
        if not self.remaining_operations:
            return

        op_type, cycles = self.remaining_operations[0]

        # Check again if the operation is I/O
        if op_type == 'I/O':
            self.io_time_remaining = cycles  # Set the I/O time remaining
            self.remaining_operations.pop(0)  # Remove the I/O operation from the list of remaining operations
            self.completed_operations += 1  # Increment completed operations counter

    # Progress ongoing I/O operation by 1 time unit
    def progress_io(self):
        # Check if there is stil I/O operation for the job
        if self.io_time_remaining > 0:
            self.io_time_remaining -= 1  # Decrease the remaining I/O time by 1 unit

